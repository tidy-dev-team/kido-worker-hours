let _lang = 'he';
const _dicts = { he, en };

export function t(key) {
  return (_dicts[_lang] || he)[key] || (_dicts.he)[key] || key;
}

export function getLang() { return _lang; }

export function setLang(lang) {
  _lang = lang === 'en' ? 'en' : 'he';
  document.documentElement.lang = _lang;
  document.documentElement.dir = _lang === 'he' ? 'rtl' : 'ltr';
}

// Helper: month key → localized label  e.g. "2026-04" → "April 2026" or "אפריל 2026"
export function monthLabel(mk) {
  const [y, m] = mk.split('-').map(Number);
  return t('month.' + m) + ' ' + y;
}

// Helper: month key → localized short  e.g. "2026-04" → "Apr" or "אפריל"
export function monthShort(mk) {
  const [, m] = mk.split('-').map(Number);
  return t('monthShort.' + m);
}
